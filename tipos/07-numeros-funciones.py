import math  # import a module

print(round(1.3))
print(round(1.5))
print(abs(-45))  # Absolute value
# These 3 functions no need math

# Python doesn't have a lot of methos to work with numbers
# But, it has a module to do that in a better way


print(math.ceil(1.1))  # return the closest upper number
print(math.floor(1.9999))  # return the closest lowwer number
print(math.isnan(45))  # return if a value is a number. Only receive real numbers
print(math.pow(10, 3))  # return the power of 2 numbers
print(math.sqrt(9))  # return the square root of a number

